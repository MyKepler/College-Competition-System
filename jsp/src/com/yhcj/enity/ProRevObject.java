package com.yhcj.enity;

public class ProRevObject {
		public String id;
		public String project_id;
		public String review_type;
		public String review_msg;
		public String review_code;
		public String review_user_id;
		public String review_user_name;
		public String getId() {
			return id;
		}
		public void setId(String id) {
			this.id = id;
		}
		public String getProject_id() {
			return project_id;
		}
		public void setProject_id(String project_id) {
			this.project_id = project_id;
		}
		public String getReview_user_name() {
			return review_user_name;
		}
		public void setReview_user_name(String review_user_name) {
			this.review_user_name = review_user_name;
		}
		public String getReview_type() {
			return review_type;
		}
		public void setReview_type(String review_type) {
			this.review_type = review_type;
		}
		public String getReview_msg() {
			return review_msg;
		}
		public void setReview_msg(String review_msg) {
			this.review_msg = review_msg;
		}
		public String getReview_code() {
			return review_code;
		}
		public void setReview_code(String review_code) {
			this.review_code = review_code;
		}
		public String getReview_user_id() {
			return review_user_id;
		}
		public void setReview_user_id(String review_user_id) {
			this.review_user_id = review_user_id;
		}
}
