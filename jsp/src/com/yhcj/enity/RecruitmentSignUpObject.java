package com.yhcj.enity;

public class RecruitmentSignUpObject {
	
	public String sign_up_id;
	public String project_id;
	public String user_id;
	public String user_name;
	public String email;
	public String phone_number;
	public String sign_up_time;
	public String state;
	public String getSign_up_id() {
		return sign_up_id;
	}
	public void setSign_up_id(String sign_up_id) {
		this.sign_up_id = sign_up_id;
	}
	public String getProject_id() {
		return project_id;
	}
	public void setProject_id(String project_id) {
		this.project_id = project_id;
	}
	public String getUser_id() {
		return user_id;
	}
	public void setUser_id(String user_id) {
		this.user_id = user_id;
	}
	public String getUser_name() {
		return user_name;
	}
	public void setUser_name(String user_name) {
		this.user_name = user_name;
	}
	public String getEmail() {
		return email;
	}
	public void setEmail(String email) {
		this.email = email;
	}
	public String getPhone_number() {
		return phone_number;
	}
	public void setPhone_number(String phone_number) {
		this.phone_number = phone_number;
	}
	public String getSign_up_time() {
		return sign_up_time;
	}
	public void setSign_up_time(String sign_up_time) {
		this.sign_up_time = sign_up_time;
	}
	public String getState() {
		return state;
	}
	public void setState(String state) {
		this.state = state;
	}

}
